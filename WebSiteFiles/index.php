      <h2>What is it?</h2>
      <p>Sports Tactics Board is a utility that allows coaches, 
      trainers and officials to describe sports tactics, strategies 
      and positioning using a magnetic or chalk-board style approach. 
      Supports soccer/football, ice hockey, futsal, floorball, volleyball 
      and American football so far, other sports can be easily added.</p>
      
      <p>For a list of current and planned features, 
      please look <a href="features.php">here</a>.</p>

      <p>You can also take a look at the <a href="readme.txt">readme.txt</a> file 
      which provides more information on the tool.</p>
    
      <h2>How do I get a copy to use?</h2>
      <ol>
        <li>Get the Microsoft .NET 3.5 Framework - this is often already installed on up-to-date Windows computers.</li>
        <li>Download a release of Sports Tactics Board from the <a href="downloads.php">Downloads</a> page.</li>
      </ol>
      <p>The Microsoft&nbsp;.NET&nbsp;3.5&nbsp;Framework can be downloaded 
      from Microsoft using Windows Update or Microsoft Update, or from
      the <a href="http://www.microsoft.com/downloads/details.aspx?familyid=AB99342F-5D1A-413D-8319-81DA479AB0D7&displaylang=en">Microsoft Download Center</a>.</p>
      <p>You can also obtain the source code for Sports Tactics Board using Subversion by 
      following the instructions on <a href="http://sourceforge.net/svn/?group_id=181975">this page</a>.</p>

      <h2>Documentation</h2>
